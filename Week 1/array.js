// fruits array

// Index   =     0    ,     1    ,     2
let fruits = ['apples', 'oranges', 'bananas'];
console.log(fruits[0]); // prints 'apples'

fruits.push('grapes'); // adds 'grapes' to index 3 of array -- end of array 
console.log(fruits);

fruits.pop(); // removes 'grapes' from array -- removes element at end of array
console.log(fruits);